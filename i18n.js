  const dictionaries = { "zh-CN": zh, en, ja, ko, fr, de, es, pt, ru, ar };
  const page = document.documentElement.dataset.page || "home";
  const select = document.querySelector("#language-select");
  const saved = localStorage.getItem("vagatools-language") || "auto";

  function normalize(locale) {
    const lower = String(locale || "").toLowerCase();
    return localeAliases[lower] || localeAliases[lower.split("-")[0]] || "en";
  }

  function currentLanguage(choice) {
    if (choice && choice !== "auto") return normalize(choice);
    return normalize(navigator.languages && navigator.languages[0] ? navigator.languages[0] : navigator.language);
  }

  function translate(choice) {
    const language = currentLanguage(choice);
    const dict = dictionaries[language] || en;
    document.documentElement.lang = language;
    document.documentElement.dir = language === "ar" ? "rtl" : "ltr";

    document.querySelectorAll("[data-i18n]").forEach((node) => {
      const key = node.dataset.i18n;
      node.textContent = dict[key] || en[key] || zh[key] || node.textContent;
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
      const key = node.dataset.i18nPlaceholder;
      node.setAttribute("placeholder", dict[key] || en[key] || zh[key] || node.getAttribute("placeholder") || "");
    });

    const title = dict[`meta.title.${page}`] || en[`meta.title.${page}`];
    const description = dict[`meta.description.${page}`] || en[`meta.description.${page}`];
    if (title) document.title = title;
    const descriptionNode = document.querySelector('meta[name="description"]');
    if (descriptionNode && description) descriptionNode.setAttribute("content", description);
  }

  if (select) {
    select.value = Array.from(select.options).some((option) => option.value === saved) ? saved : "auto";
    select.addEventListener("change", () => {
      localStorage.setItem("vagatools-language", select.value);
      translate(select.value);
    });
  }

